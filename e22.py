bool_list = [True, False, True]
print(all(bool_list)) #czy wszystkie wartości na liście bool_list są true?
print(not all(bool_list)) #zaprzeczenie all i tego co zwróciła ta metoda (all)
print(any(bool_list)) #czy jakikolwiek element bool_list jest True?

